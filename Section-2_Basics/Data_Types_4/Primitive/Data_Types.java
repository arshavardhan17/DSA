package Data_Types_4.Primitive;

public class Data_Types {
    public static void main(String[] args) {
        // Declare and initialize variables of different data types
        int integerVariable = 42; // Integer type
        double doubleVariable = 3.14; // Double type
        float floatVariable = 2.5f; // Float type
        char charVariable = 'A'; // Character type
        boolean booleanVariable = true; // Boolean type

        // Print the values of the variables
        System.out.println("Integer: " + integerVariable);
        System.out.println("Double: " + doubleVariable);
        System.out.println("Float: " + floatVariable);
        System.out.println("Character: " + charVariable);
        System.out.println("Boolean: " + booleanVariable);
    }
    
}

